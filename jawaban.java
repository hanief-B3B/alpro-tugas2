public class jawaban {
    public static void main(String[] args) {
        // tipe data primitif
        byte tipeByte = 127;
        short tipeShort = 32000; 
        int tipeInt = 2147483647;
        long tipeLong = 9223372036854775807L; 
        float tipeFloat = 3.14f; 
        double tipeDouble = 3.141592653589793; 
        char tipeChar = 'A'; 
        boolean tipeBoolean = true;

        // tipe data referensi
        String tipeString = "Ini contoh tipe data String!";

        // menampilkan nilai dari tiap tipe data
        System.out.println("Contoh Tipe Data di Java:");
        System.out.println("1. byte    : " + tipeByte);
        System.out.println("2. short   : " + tipeShort);
        System.out.println("3. int     : " + tipeInt);
        System.out.println("4. long    : " + tipeLong);
        System.out.println("5. float   : " + tipeFloat);
        System.out.println("6. double  : " + tipeDouble);
        System.out.println("7. char    : " + tipeChar);
        System.out.println("8. boolean : " + tipeBoolean);
        System.out.println("9. String  : " + tipeString);
    }
}

//Tipe data primitif:
//byte: 8-bit, tipe data bilangan bulat rentang nilai dari -128 hingga 127.
//short: 16-bit, tipe data bilangan bulat rentang nilai dari -32,768 hingga 32,767.
//int: 32-bit, tipe data bilangan bulat rentang nilai dari -2^31 hingga 2^31-1.
//long: 64-bit, tipe data bilangan bulat rentang nilai dari -2^63 hingga 2^63-1.
//float: 32-bit, tipe data pecahan dengan presisi tunggal.
//double: 64-bit, tipe data pecahan dengan presisi ganda.
//char: 16-bit, menyimpan satu karakter Unicode.
//boolean: hanya memiliki dua nilai, yaitu true atau false.

//Tipe data referensi:
//String: digunakan untuk menyimpan teks.
